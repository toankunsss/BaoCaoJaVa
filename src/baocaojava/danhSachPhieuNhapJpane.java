    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = phieunhaptable.getSelectedRow();
        if (selectedRow >= 0) {
            // Lấy giá trị từ bảng phieunhaptable
            String maPhieu = phieunhaptable.getValueAt(selectedRow, 1).toString();
            String nguoiTao = phieunhaptable.getValueAt(selectedRow, 3).toString();
            String thoiGianTao = phieunhaptable.getValueAt(selectedRow, 4).toString();
            String nhaCungCap = phieunhaptable.getValueAt(selectedRow, 2).toString();
            String tongTien = phieunhaptable.getValueAt(selectedRow, 5).toString();

            // Gửi dữ liệu sang ChiTietJPanel
            ChiTietJPanel chiTietPanel = new ChiTietJPanel();
            chiTietPanel.setMaPhieu(maPhieu);
            chiTietPanel.setNguoiTao(nguoiTao);
            chiTietPanel.setThoiGianTao(thoiGianTao);
            chiTietPanel.setNhaCungCap(nhaCungCap);
            chiTietPanel.setTongTien(tongTien);
            chiTietPanel.loadDataToTable(); // Tải lại bảng chi tiết
            JFrame frame = new JFrame("Chi Tiết Phiếu Nhập");
            frame.add(chiTietPanel);
            frame.pack();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một hàng trong bảng!");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

