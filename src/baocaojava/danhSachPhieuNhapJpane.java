    private void loadDataToTable() {
        defaultTableModel.setRowCount(0);
        ArrayList<phieuModel> list = phieuNhapController.selectAll();
        int i = 0;
        for (phieuModel sp : list) {
            i++;
            defaultTableModel.addRow(new Object[]{
                i,
                sp.getMaPN(),
                sp.getMaNCC(),
                sp.getNguoiTao(),
                sp.getThoiGianTao(),
                sp.getTongTien()});

        }
    }

