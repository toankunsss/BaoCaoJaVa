    private void loadDataToTable() {
        ArrayList<sanpham> list = sanPhamController.selectAll();
        for (sanpham sp : list) {
            defaultTableModel.addRow(new Object[]{
                sp.getMaSua(),
                sp.getTenSua(),
                sp.getSoLuong(),
                sp.getSize(),
                sp.getKhoiLuong(),
                sp.getDonGia(),
                sp.getNgayHetHan()
            });

        }
    }

    public void updateFieldsFromSelectedRow() {
        int row = sanPhamTable.getSelectedRow();

        // Kiểm tra nếu hàng được chọn hợp lệ
        if (row >= 0) {
            String sp = String.valueOf(sanPhamTable.getValueAt(row, 0));
            sanpham sanPhamUD = sanPhamController.selectById(sp);

            maSpTextFiled.setText(sanPhamUD.getMaSua());
            tenSpTextField.setText(sanPhamUD.getTenSua());
            soluongSpn.setValue(sanPhamUD.getSoLuong());
            kichCocbx.setSelectedItem(sanPhamUD.getSize());
            donGiaTextField.setText(String.valueOf(sanPhamUD.getDonGia()));
            // Thiết lập giá trị cho JDateChooser
            if (sanPhamUD.getNgayHetHan() != null) {
                hanSuDungDate.setDate(sanPhamUD.getNgayHetHan());
            }
            maSpTextFiled.setEditable(false);
        }
    }

