    public String CreatId() {
        sanPhamController = new SanPhamController();
        ArrayList<sanpham> arr = sanPhamController.selectAll();
        String check = "";
        int id = arr.size() + 1;
        if (arr != null) {
            for (sanpham nhanVienmodel : arr) {
                if (nhanVienmodel.getMaSua().equals("S" + id)) {
                    check = nhanVienmodel.getMaSua();
                }
            }
            while (check.length() != 0) {
                id++;
                for (int i = 0; i <= arr.size(); i++) {
                    if (arr.get(i).getMaSua().equals("NV" + id)) {
                        check = arr.get(i).getMaSua();
                    } else {
                        check = "";
                    }
                }
            }
            return "S" + id;
        } else {
            return "S" + 1;
        }
    }

