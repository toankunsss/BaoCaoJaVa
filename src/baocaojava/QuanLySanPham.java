    private void deleteJbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJbtnActionPerformed
        // TODO add your handling code here:
        // Kiểm tra nếu có hàng nào được chọn
        int row = sanPhamTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm muốn xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            // Xác nhận trước khi xóa
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sản phẩm này không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Lấy mã sản phẩm từ hàng đã chọn
                String maSp = String.valueOf(sanPhamTable.getValueAt(row, 0));
                sanPhamController.XoaSanPham(maSp);

                // Cập nhật lại bảng sau khi xóa
                defaultTableModel.setRowCount(0);
                ArrayList<sanpham> sanPhamList = sanPhamController.selectAll();
                for (sanpham sp : sanPhamList) {
                    defaultTableModel.addRow(new Object[]{
                        sp.getMaSua(), sp.getTenSua(), sp.getSize(), sp.getMaNCC(), sp.getSoLuong(), sp.getDonGia(), sp.getNgayHetHan(), sp.getAnh()
                    });
                }

                JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteJbtnActionPerformed

