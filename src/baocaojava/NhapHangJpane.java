    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if (row != -1) {
            // Lấy số lượng và đơn giá của sản phẩm sẽ xóa
            int soLuong = (int) bangPN.getValueAt(row, 3);
            double donGia = (double) bangPN.getValueAt(row, 4);

            // Tính tổng tiền của sản phẩm sẽ xóa
            double tongTienXoa = soLuong * donGia;

            // Cập nhật tổng tiền
            double currentTotal = Double.parseDouble(jLabel6.getText().replace("đ", "").trim());
            currentTotal -= tongTienXoa;

            // Xóa dòng khỏi jTable2
            bangPN.removeRow(row);

            // Cập nhật lại hiển thị tổng tiền
            jLabel6.setText(currentTotal + "đ");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để xóa.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

