    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        phiModel.setMaPN(maPhieuTextField.getText());
        phiModel.setNguoiTao(nguoiTaoPhieuTextField.getText());
        phiModel.setMaNCC((String) nhaCungCapcbx.getSelectedItem());
        // Lấy giá trị từ JLabel tổng tiền
        String tongTienText = jLabel6.getText();
        // Loại bỏ ký tự "đ" và khoảng trắng
        tongTienText = tongTienText.replace("đ", "").trim();
        // Chuyển đổi chuỗi thành double
        double tongTien = Double.parseDouble(tongTienText);
        phiModel.setTongTien(tongTien);
        phiModel.setThoiGianTao(new Timestamp(System.currentTimeMillis())); // Thiết lập thời gian hiện tại
        // Gọi controller để thêm phiếu
        try {
            phieuNhapController.themPhieu(phiModel);
            JOptionPane.showMessageDialog(this, "Thêm phiếu thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Thêm phiếu thất bại: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        ChiTietPhieuNhapDAO dao = new ChiTietPhieuNhapDAO(); // Tạo đối tượng DAO

        for (int row = 0; row < jTable2.getRowCount(); row++) {
            String maPN = maPhieuTextField.getText();
            String maSua = jTable2.getValueAt(row, 1).toString();
            int soLuong = Integer.parseInt(jTable2.getValueAt(row, 3).toString());
            double donGia = Double.parseDouble(jTable2.getValueAt(row, 4).toString());

            // Tạo đối tượng ChiTieuPhieumodel
            ChiTieuPhieumodel chiTietPhieu = new ChiTieuPhieumodel(maPN, maSua, soLuong, donGia);

            // Gọi phương thức thêm vào cơ sở dữ liệu
            int ketQua = dao.them(chiTietPhieu);
            if (ketQua > 0) {
                System.out.println("Thêm thành công cho dòng: " + row);
            } else {
                System.out.println("Thêm không thành công cho dòng: " + row);
            }
        }
        loadDataToTable();


    }//GEN-LAST:event_jButton4ActionPerformed

