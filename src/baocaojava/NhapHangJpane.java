    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow(); // Lấy hàng được chọn từ jTable1
        if (row != -1) {
            // Lấy thông tin sản phẩm từ jTable1
            String maSua = (String) bangSP.getValueAt(row, 0);
            String tenSua = (String) bangSP.getValueAt(row, 1);
            double donGia = (double) bangSP.getValueAt(row, 3);

            // Lấy số lượng từ jSpinner1
            int soLuong = (int) jSpinner1.getValue();

            // Tạo STT tự động
            int stt = bangPN.getRowCount() + 1;

            // Thêm dòng vào jTable2
            bangPN.addRow(new Object[]{stt, maSua, tenSua, soLuong, donGia});

            // Tính tổng tiền cho sản phẩm vừa thêm
            double tongTien = soLuong * donGia;

            // Cập nhật tổng tiền vào một JLabel hoặc JTextField
            double currentTotal = Double.parseDouble(jLabel6.getText().replace("đ", "").trim());
            currentTotal += tongTien;

            // Cập nhật lại hiển thị tổng tiền
            jLabel6.setText(currentTotal + "đ");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sản phẩm để thêm.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

