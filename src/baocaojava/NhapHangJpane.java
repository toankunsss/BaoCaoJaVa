    public String CreatId() {
        phieuNhapController = new PhieuNhapController();
        ArrayList<phieuModel> arr = phieuNhapController.selectAll();
        String check = "";
        int id = arr.size() + 1;
        if (arr != null) {
            for (phieuModel nhanVienmodel : arr) {
                if (nhanVienmodel.getMaPN().equals("PN" + id)) {
                    check = nhanVienmodel.getMaPN();
                }
            }
            while (check.length() != 0) {
                id++;
                for (int i = 0; i <= arr.size(); i++) {
                    if (arr.get(i).getMaPN().equals("NV" + id)) {
                        check = arr.get(i).getMaPN();
                    } else {
                        check = "";
                    }
                }
            }
            return "PN" + id;
        } else {
            return "PN" + 1;
        }
    }

